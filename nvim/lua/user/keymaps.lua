-- Normal --
-- Better window navigation
keymap("n", "<C-h>", "<C-w>h", opts)
keymap("n", "<C-j>", "<C-w>j", opts)
keymap("n", "<C-k>", "<C-w>k", opts)
keymap("n", "<C-l>", "<C-w>l", opts)
keymap("n", "<Tab>", ":bnext<CR>", opts)
keymap("n", "<S-t>", ":bd<CR>", opts)
keymap("n", "<C-s>", ":w<CR>", opts)
keymap("n", "<C-S-s>", ":wa<CR>", opts)
keymap("n", "<C-w>", ":q!<CR>", opts)

-- Telescope
keymap("n", "<C-f>", ":Telescope find_files<CR>", opts)

-- Nvim-tree
keymap("n", "<C-p>", ":NvimTreeToggle<CR>", opts)
keymap("n", "<C-r>",":NvimTreeFocus<CR>",opts)

-- ToggleTerm
keymap("n", "<C-o>", ":ToggleTerm size=10 dir=. direction=horizontal<CR>", opts)
keymap("n", "<C-t>", ":ToggleTerm<CR>", opts)

-- Resize with arrows
keymap("n", "<C-Up>", ":resize -2<CR>", opts)
keymap("n", "<C-Down>", ":resize +2<CR>", opts)
keymap("n", "<C-Left>", ":vertical resize -2<CR>", opts)
keymap("n", "<C-Right>", ":vertical resize +2<CR>", opts)

-- Navigate buffers
keymap("n", "<S-l>", ":bnext<CR>", opts)
keymap("n", "<S-h>", ":bprevious<CR>", opts)

-- Move text up and down
keymap("n", "<A-j>", "<Esc>:m .+1<CR>==gi", opts)
keymap("n", "<A-k>", "<Esc>:m .-2<CR>==gi", opts)

-- Insert --
-- Press jk fast to enter
keymap("i", "jk", "<ESC>", opts)

-- Visual --
-- Stay in indent mode
keymap("v", "<", "<gv", opts)
keymap("v", ">", ">gv", opts)

-- Move text up and down
keymap("v", "<A-j>", ":m .+1<CR>==", opts)
keymap("v", "<A-k>", ":m .-2<CR>==", opts)
keymap("v", "p", '"_dP', opts)

-- Visual Block --
-- Move text up and down
keymap("x", "J", ":move '>+1<CR>gv-gv", opts)
keymap("x", "K", ":move '<-2<CR>gv-gv", opts)
keymap("x", "<A-j>", ":move '>+1<CR>gv-gv", opts)
keymap("x", "<A-k>", ":move '<-2<CR>gv-gv", opts)

-- Copy to clipboard
keymap("v", "<leader-y>", "+y",opts)
keymap("v", "<leader-Y>", "+yg",opts)
keymap("v", "<leader-y>", "+yy",opts)
keymap("v", "<leader -y>", "+y",opts)

-- Paste from clipboard
keymap("v", "<leader-y>", "+p",opts)
keymap("v", "<leader-y>", "+P",opts)


